import java.util.Scanner;

public class Main {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);

		int n = sc.nextInt();
		int result = n;
		
		if (n == 0) {
			System.out.println("1");
		}
		
		else {
			for (int i=n; i!=1; i--) {
				result *= i-1;
			}
			System.out.println(result);
		}
		
		sc.close();
	}

}
